package jalali

import "testing"

func TestFormatDateTimeKnownValues(t *testing.T) {
	tests := []struct {
		input    string
		expected string
	}{
		{"2026-02-25 18:49:45", "1404/12/06 18:49:45"},
		{"2026-03-21 00:00:00", "1405/01/01 00:00:00"},
		{"2025-03-20 12:30:00", "1403/12/30 12:30:00"},
		{"2024-01-01 08:15:30", "1402/10/11 08:15:30"},
		{"2024-03-20 23:59:59", "1403/01/01 23:59:59"},
		{"2024-06-15 14:22:10", "1403/03/26 14:22:10"},
		{"2024-09-23 06:00:00", "1403/07/02 06:00:00"},
		{"2024-12-31 23:59:59", "1403/10/11 23:59:59"},
		{"2025-01-01 00:00:01", "1403/10/12 00:00:01"},
		{"2025-06-10 11:11:11", "1404/03/20 11:11:11"},
		{"2025-09-01 07:45:00", "1404/06/10 07:45:00"},
		{"2025-12-31 18:00:00", "1404/10/10 18:00:00"},
		{"2026-01-15 03:20:40", "1404/10/25 03:20:40"},
		{"2026-07-14 09:17:00", "1405/04/23 09:17:00"},
		{"2023-03-21 12:00:00", "1402/01/01 12:00:00"},
		{"2023-06-01 00:00:00", "1402/03/11 00:00:00"},
		{"2022-11-05 16:40:22", "1401/08/14 16:40:22"},
		{"2027-01-01 00:00:00", "1405/10/11 00:00:00"},
		{"2027-03-21 15:30:00", "1406/01/01 15:30:00"},
		{"2020-05-18 20:05:55", "1399/02/29 20:05:55"},
		{"2019-12-29 01:02:03", "1398/10/08 01:02:03"},
		{"2028-02-29 12:00:00", "1406/12/10 12:00:00"},
		{"2021-08-08 08:08:08", "1400/05/17 08:08:08"},
		{"2025-07-15 09:17:00", "1404/04/24 09:17:00"},
	}

	for _, tc := range tests {
		got, err := FormatDateTime(tc.input)
		if err != nil {
			t.Fatalf("input %s: %v", tc.input, err)
		}
		if got != tc.expected {
			t.Fatalf("input %s: expected %s got %s", tc.input, tc.expected, got)
		}
	}
}
